print(w, x, y, z, sep='-')
print(w, x, y, z, sep=':')
print(w, x, y, z, sep='/')
print(w, x, y, z, sep='*')
print(w, x, y, z, sep='----')
print(w, x, y, z, sep='**')
print(w, x, y, z, sep='=')